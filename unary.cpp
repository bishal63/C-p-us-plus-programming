#include<iostream>
#include<conio.h>
using namespace std;
int main()
{
    int a=+6;
    cout<<a <<endl;
    int b=-6;
    cout<<b <<endl;
    int c=6;
    cout<<++c <<endl;   //increment
    int d=6;
    cout<<--d <<endl;          //decrement
    int e=6;
    cout<<e++ <<endl;        //increment
    cout<<e <<endl;
    int f=6;
    cout<<f-- <<endl;       //decrement
    cout<<f;


    getch();
}
